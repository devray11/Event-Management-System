      <h3>GCET</h3>
      <p><strong>Shri Gordhanbhai Hathibhai Patel</strong>, an eminent philanthropist of Gujarat, made a generous donation of Rupees Three Crores for the new college. In honour of this magnanimous gesture, the college has been named G H Patel College of Engineering and Technology, popularly known as GCET. GCET is managed by Charutar Vidya Mandal (CVM), a registered charitable trust established in 1945. Since its inception the trust has been striving for the upliftment of rural society through education.  
      This college was affiliated to Sardar Patel University from the year 1996 to 2008. later on from the year 2008, Institute is affiliated to Gujarat Technological University. From 2020 Institute is affiliated to the Charutar Vidyamandal University. <strong>(Website: https://www.gcet.ac.in)</strong> </p>
