      <h3>CVMU</h3>
      <p><strong>Charutar Vidhya Mandal</strong>, took place on the eve of Independence, when the illuminating vision of the great Sardar Patel was received with incredible enthusiasm by Shri Bhaikaka and Shri Bhikhabhai saheb. They dedicated their lives to the cause of rural regeneration by establishing Gramodyog and by bringing higher education to the doorstep of village community. On March 03, 1945, they sat down to draw up a plan of an educational township – a Vidyanagar – which was later christened as Vallabh Vidyanagar in the form of a tribute to the great son of the soil, Sardar Vallabhbhai Patel. It was not only the first planned University Township of independent India, but also a comprehensive hub of higher education that provided schools and College of all major academic disciplines and had elaborate plans for hostels, faculty quarters, play grounds and parks. It was a dream scheme in the real sense of the term. <strong>(Website: https://www.cvmu.edu.in)</strong> </p>
